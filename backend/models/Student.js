const mongoose = require('mongoose');

const StudentSchema = new mongoose.Schema({
  name: { type: String, required: true },
  email: { type: String, required: true, unique: true },
  
  // University specific identifiers
  prnNumber: { type: String, required: true, unique: true }, 
  branch: { type: String, enum: ['Mechatronics', 'Computer', 'Civil', 'Mechanical', 'Electrical'], required: true },
  year: { type: Number, required: true }, // e.g., 1 for First Year
  
  // For the Python WhatsApp Alert Microservice
  parentPhone: { type: String, required: true }, 
  
  // Security: The persistent hash generated by FingerprintJS on their phone
  registeredDeviceId: { type: String }, 
  
  // References to the classes this student is enrolled in
  enrolledClasses: [{ type: mongoose.Schema.Types.ObjectId, ref: 'Class' }]
}, { timestamps: true });

module.exports = mongoose.model('Student', StudentSchema);
